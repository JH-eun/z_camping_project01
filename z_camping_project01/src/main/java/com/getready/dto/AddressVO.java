package com.getready.dto;

public class AddressVO {
	private String postar_code;
	private String sido;
	private String gugun;
	private String gil;
	private String ro;
	
	public String getPostar_code() {
		return postar_code;
	}
	public void setPostar_code(String postar_code) {
		this.postar_code = postar_code;
	}
	public String getSido() {
		return sido;
	}
	public void setSido(String sido) {
		this.sido = sido;
	}
	public String getGugun() {
		return gugun;
	}
	public void setGugun(String gugun) {
		this.gugun = gugun;
	}
	public String getGil() {
		return gil;
	}
	public void setGil(String gil) {
		this.gil = gil;
	}
	public String getRo() {
		return ro;
	}
	public void setRo(String ro) {
		this.ro = ro;
	}
	
	

}
